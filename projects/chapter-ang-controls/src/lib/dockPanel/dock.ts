/**
 * Enum representing the possible docking positions for child elements in the DockPanelComponent.
 */
export enum Dock {
    /**
     * Dock the element to the top of the container.
     */
    Top = 'top',

    /**
     * Dock the element to the bottom of the container.
     */
    Bottom = 'bottom',

    /**
     * Dock the element to the left of the container.
     */
    Left = 'left',

    /**
     * Dock the element to the right of the container.
     */
    Right = 'right'
}
